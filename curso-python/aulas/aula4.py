# Tipos INT e FLOAT

'''

int = número inteiro. (números sem casas decimais)
float = números com casas decimais. (números com pontos flutuantes)

'''
# Números inteiros. (INT)

print(type(0))
print(11, 10000)
print(-11, 1, 2, 3, -7,'\n')

# Números com casas decimais (FLOAT).

print(type(1.1))
print(1.1, 10,11)
print(0.7, -0.8,'\n')

# A função type() mostra o tipoe que o Python inferiu ao valor.

print(type('1'))
print(type(1))
print(type(-1))
print(type(1.1))
print(type(-1.1))
